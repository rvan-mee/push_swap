#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(int))
{
	t_list	*next;

	while (*lst)
	{
		next = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = next;
	}
	lst = NULL;
}
